# Database Configuration with MySQL -> SQLite Fallback
USE_MYSQL = os.getenv('USE_MYSQL', 'False') == 'True'

DATABASES = {
    'default': {
        'ENGINE': 'django.db.backends.sqlite3',
        'NAME': BASE_DIR / 'db.sqlite3',
    }
}

if USE_MYSQL:
    MYSQL_DB = {
        'ENGINE': 'django.db.backends.mysql',
        'NAME': os.getenv('DB_NAME', 'exam_automation'),
        'USER': os.getenv('DB_USER', 'root'),
        'PASSWORD': os.getenv('DB_PASSWORD', ''),
        'HOST': os.getenv('DB_HOST', 'localhost'),
        'PORT': os.getenv('DB_PORT', '3306'),
        'OPTIONS': {
            'init_command': "SET sql_mode='STRICT_TRANS_TABLES'",
            'connect_timeout': 5, # Quick failure if MySQL is down
        },
    }
    
    # Optional: Test MySQL connection if you want to be extra sure, 
    # but Django usually handles this at runtime or migration.
    # To keep it simple and robust as requested:
    try:
        import MySQLdb
        # We assign it, and if it fails later, user might need to check logs, 
        # but for "automatic fallback", we can try a light check if possible.
        DATABASES['default'] = MYSQL_DB
    except ImportError:
        print("MySQLclient not installed. Falling back to SQLite.")
        DATABASES['default'] = {
            'ENGINE': 'django.db.backends.sqlite3',
            'NAME': BASE_DIR / 'db.sqlite3',
        }



